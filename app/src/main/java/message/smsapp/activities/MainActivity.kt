import android.view.MenuItem
import android.view.WindowManager
import android.widget.ListView
import android.widget.TextView
import message.smsapp.R
import message.smsapp.message.ConversationManager

class MainActivity : AppCompatActivity() {
    companion object {
        private const val REQUEST_PERMISSION_KEY = 1
    }

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        window.addFlags(WindowManager.LayoutParams.FLAG_KEEP_SCREEN_ON)
        setContentView(R.layout.activity_main)

        findViewById<TextView>(R.id.textView).text = getString(R.string.message_title)

        ConversationManager(this, findViewById(R.id.list_Messages), REQUEST_PERMISSION_KEY)
    }

    override fun onRequestPermissionsResult(requestCode: Int,
                                            permissions: Array<String>, grantResults: IntArray) {
        when (requestCode) {
            REQUEST_PERMISSION_KEY -> {

                // If request is cancelled, the result arrays are empty.
                if (grantResults.isNotEmpty()
                        && grantResults[0] == PackageManager.PERMISSION_GRANTED) {
                    // permission was granted, yay! Do the
                    // contacts-related task you need to do.
                } else {
                    // permission denied, boo! Disable the
                    // functionality that depends on this permission.
                }
                return
            }
        }
    }
}